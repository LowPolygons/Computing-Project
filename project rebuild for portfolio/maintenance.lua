function maintenance:init()
	if love.filesystem.getInfo("multipliers.sfl") then
		local mults, size = filehandling:readFileData("multipliers.sfl")
		local data = filehandling:reformatter(mults)
		simulation.multipliers = data
	else
		simulation.multipliers = self.multipliers
		_, _ = filehandling:storeData(self.multipliers, "multipliers.sfl")
	end
	
	if love.filesystem.getInfo("affectors.sfl") then
		local affs, size = filehandling:readFileData("affectors.sfl")
		local data = filehandling:reformatter(affs)
		simulation.affectors = data
	else
		simulation.affectors = self.affectors
		_, _ = filehandling:storeData(self.affectors, "affectors.sfl")
	end
end

